import java.lang.*;
import java.util.*;
class P35SwitchCase 
{
    public static void main(String args[])
    {
        int n=3;
        switch(n)
        {
            case 1: System.out.println("One");   
                break;
            case 2: System.out.println("Two");
                break;
            case 3: System.out.println("Three");
                break;
            case 4: System.out.println("Four");
                break;
            default : System.out.println("Invalid");
        }
    }
}
